function dompierMusic(switches) {
  const notes = {
    '0100000101': 'C4',
    '0100100110': 'D4',
    '0101001001': 'E4',
    '0101011101' : 'F4',
    '0110001000': 'G4',
    '0110111000': 'A4',
    '0111101110': 'B4',
    '1000001011': 'C5',
    '0000000000': 'REST'
  }
  let output = [];
  for(let i=0;i<switches.length;i++){
    output.push(notes[switches[i]]);
  }
  return output;
}

